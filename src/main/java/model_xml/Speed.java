package model_xml;

public class Speed {
    public double getvalue() {
        return this.value;
    }

    public void setvalue(double value) {
        this.value = value;
    }

    double value;

    public String getunit() {
        return this.unit;
    }

    public void setunit(String unit) {
        this.unit = unit;
    }

    String unit;

    public String getname() {
        return this.name;
    }

    public void setname(String name) {
        this.name = name;
    }

    String name;
}
